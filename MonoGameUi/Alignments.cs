namespace MonoGameUi
{
    /// <summary>
    /// Liste von Möglichkeiten zum horizontalen Alignment.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// Links angeordnet
        /// </summary>
        Left,

        /// <summary>
        /// Rechts angeordnet
        /// </summary>
        Right,

        /// <summary>
        /// Zentriert
        /// </summary>
        Center,

        /// <summary>
        /// Auf voller Breite
        /// </summary>
        Stretch
    }

    /// <summary>
    /// Liste der Möglichkeiten zum vertikalen Alignment.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// Oben angeordnet
        /// </summary>
        Top,

        /// <summary>
        /// Unten angeordnet
        /// </summary>
        Bottom,

        /// <summary>
        /// Zentriert
        /// </summary>
        Center,

        /// <summary>
        /// Volle Höhe
        /// </summary>
        Stretch
    }
}
